// JavaScript function to increment the number




function incrementNumber() {
    
    // Get the current number element
    var numberElement = document.getElementById("number");

    // Get the current number value
    var currentNumber = parseInt(numberElement.innerText);

    // Increment the number
    var newNumber = currentNumber + 1;

    // Update the number element with the new value
    numberElement.innerText = newNumber;
}

